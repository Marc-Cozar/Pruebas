    public boolean EsNull(int fila, int columna){
        boolean esNull = false;
        if(table1vs1.getValueAt(fila, columna) == null){
            esNull = true;
        }
        return esNull;
    }
    
    public void Omple(int fila, int columna){

        crearMovimiento(columna, fila);
        
        if(jugaX){
            table1vs1.setValueAt("X", fila, columna);
            jugaX = false;
            jugaO = true;
        }else if (jugaO){
            table1vs1.setValueAt("O", fila, columna);
            jugaO = false;
            jugaX = true;
        }
        contador++;
        
    }
    
