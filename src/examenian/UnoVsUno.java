    private void table1vs1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table1vs1MouseClicked
        // TODO add your handling code here:
        
        int fila = ObtenirFilaClicada();
        int columna = ObtenirColumnaClicada();
        
        if (NoEstaComplert()){
            if(jugaX && EsNull(fila, columna)==true){
                Omple(fila, columna);
                ComprobaGuanyador();
            }else if (jugaO && EsNull(fila,columna)){
                Omple(fila, columna);
                ComprobaGuanyador();
            }else{
                MostraError();
            }
            if(!NoEstaComplert()){
                MostrarEmpat();
                
                Home fra = new Home();
                fra.setVisible(true);
                dispose();
            }
        }
    }//GEN-LAST:event_table1vs1MouseClicked

